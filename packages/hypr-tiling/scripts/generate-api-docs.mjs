// Generates the curated public API reference consumed by the homepage `/docs`
// route. Pipeline (all deterministic so the committed snapshot never drifts):
//
//   1. `api-extractor run` — refreshes the doc model (`temp/hypr-tiling.api.json`)
//      from the built `dist/*.d.ts`. Reflects the curated barrel + `@internal`
//      split, so evicted / internal symbols never reach the reference.
//   2. `api-documenter markdown` — emits one Markdown file per API item into
//      `temp/api-markdown/` (git-ignored intermediate).
//   3. This script folds the TOP-LEVEL symbol pages (interfaces already inline
//      their members) into a single render-ready TS module committed at
//      `apps/web/src/api-reference/generated.ts`. The homepage prerenders from
//      that module, so no Markdown parser ships to the client and hydration is
//      byte-stable.
//
// Regenerate with `pnpm api:docs`. CI runs the same command and fails on any
// git diff, so the snapshot cannot silently drift from the source TSDoc.

import { execFileSync } from "node:child_process";
import {
  mkdirSync,
  readdirSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";
import { marked } from "marked";

const here = dirname(fileURLToPath(import.meta.url));
const packageDir = resolve(here, "..");
const binDir = resolve(packageDir, "node_modules", ".bin");
const repoRoot = resolve(packageDir, "..", "..");
const tempDir = resolve(packageDir, "temp");
// The consumer `/docs` reference is built from the `.` entry ONLY. The
// `./devtools` and `./engine` entries write their doc models into sibling
// `temp/<entry>/` dirs (see api-extractor.{devtools,engine}.json); pointing
// api-documenter at `temp/index/` keeps them off the consumer site.
const docModelDir = resolve(tempDir, "index");
const markdownDir = resolve(tempDir, "api-markdown");
const outputFile = resolve(
  repoRoot,
  "apps",
  "web",
  "src",
  "api-reference",
  "generated.ts",
);

// The api-documenter file naming uses the UNSCOPED package name as prefix.
const PACKAGE_FILE_PREFIX = "hypr-tiling";

function runBin(bin, args) {
  const binPath = resolve(binDir, bin);
  execFileSync(binPath, args, { cwd: packageDir, stdio: "inherit" });
}

// 1. Refresh the `.` doc model. `run` (verify mode) still emits the docModel
//    JSON; it only compares the committed report, which api:check keeps current.
runBin("api-extractor", ["run", "--verbose", "-c", "api-extractor.index.json"]);

// 2. Emit per-item Markdown into a clean intermediate dir. Input is the `.`
//    doc-model dir only — devtools/engine never reach the consumer reference.
rmSync(markdownDir, { recursive: true, force: true });
mkdirSync(markdownDir, { recursive: true });
runBin("api-documenter", ["markdown", "-i", docModelDir, "-o", markdownDir]);

// 3. Fold the top-level symbol pages into a render bundle.
/**
 * @typedef {{ id: string; name: string; kind: string; html: string }} ApiSection
 */

/** Files are `hypr-tiling.<symbol>.md`; members carry an extra `.` segment. */
function isTopLevelSymbolFile(fileName) {
  if (!fileName.endsWith(".md")) {
    return false;
  }
  const stem = fileName.slice(0, -".md".length);
  const parts = stem.split(".");
  return parts.length === 2 && parts[0] === PACKAGE_FILE_PREFIX;
}

function symbolSegment(fileName) {
  return fileName.slice(0, -".md".length).split(".")[1];
}

function anchorId(segment) {
  return `api-${segment}`;
}

// Rewrite api-documenter's cross-file `.md` links into in-page anchors, drop the
// generator's boilerplate (the "do not edit" comment, breadcrumb, and the
// `<!-- -->` text separators it injects around inline code).
function normalizeMarkdown(raw) {
  const lines = raw.split("\n");
  const kept = [];
  for (const line of lines) {
    if (line.startsWith("<!-- Do not edit this file")) {
      continue;
    }
    // Breadcrumb line: `[Home](./index.md) &gt; ... &gt; [X](./x.md)`.
    if (/^\[Home\]\(\.\/index\.md\)/.test(line)) {
      continue;
    }
    kept.push(line);
  }
  let body = kept.join("\n");
  body = body.replace(/<!-- -->/g, "");
  body = body.replace(
    /\]\(\.\/hypr-tiling\.([a-z0-9_]+)(?:\.[a-z0-9_]+)*\.md\)/g,
    (_match, seg) => `](#${anchorId(seg)})`,
  );
  body = body.replace(/\]\(\.\/(?:index|hypr-tiling)\.md\)/g, "](#api-reference)");
  return body.trim();
}

// The first `## <Name> <kind>` heading names the symbol; strip it from the body
// (the route renders its own heading with the anchor) and return name + kind.
function extractHeading(markdown) {
  const match = markdown.match(/^##\s+(.+?)\s+(\w+)\s*$/m);
  if (match == null) {
    return { name: null, kind: null, body: markdown };
  }
  // API Documenter escapes `_`/`*` in heading text; restore for display.
  const name = match[1].replace(/\\([_*])/g, "$1");
  const kind = match[2];
  const body = markdown.replace(match[0], "").trim();
  return { name, kind, body };
}

marked.setOptions({ gfm: true, breaks: false });

const files = readdirSync(markdownDir)
  .filter(isTopLevelSymbolFile)
  .sort();

/** @type {ApiSection[]} */
const sections = [];
for (const fileName of files) {
  const segment = symbolSegment(fileName);
  const raw = readFileSync(resolve(markdownDir, fileName), "utf8");
  const normalized = normalizeMarkdown(raw);
  const { name, kind, body } = extractHeading(normalized);
  if (name == null || kind == null) {
    continue;
  }
  const html = marked.parse(body).trim();
  sections.push({ id: anchorId(segment), name, kind, html });
}

if (sections.length === 0) {
  throw new Error(
    "generate-api-docs: no API sections produced — check api-documenter output",
  );
}

const banner = `// AUTO-GENERATED by \`pnpm api:docs\` (packages/hypr-tiling/scripts/generate-api-docs.mjs).
// Do not edit by hand. Source of truth is the TSDoc on the public API surface;
// regenerate after changing it. CI fails on any drift from this snapshot.
`;

const typeDecls = `/** One rendered public-API symbol page (interfaces inline their members). */
export interface ApiReferenceSection {
  /** In-page anchor id (\`api-<symbol>\`), the cross-link + deep-link target. */
  readonly id: string;
  /** The symbol name, e.g. \`TilingRenderer\`. */
  readonly name: string;
  /** API item kind as emitted by API Documenter (Interface, Variable, ...). */
  readonly kind: string;
  /** Pre-rendered HTML body (headings, signature, remarks, examples, tables). */
  readonly html: string;
}
`;

const payload = `export const API_REFERENCE_SECTIONS: ReadonlyArray<ApiReferenceSection> =\n  ${JSON.stringify(
  sections,
  null,
  2,
).replace(/\n/g, "\n  ")} as const;\n`;

mkdirSync(dirname(outputFile), { recursive: true });
writeFileSync(outputFile, `${banner}\n${typeDecls}\n${payload}`, "utf8");

console.log(
  `generate-api-docs: wrote ${sections.length} API sections to ${outputFile}`,
);
